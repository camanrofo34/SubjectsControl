package Users;

public class Person {

    String id, name, lastName, birthday, mail;

    public Person(String id, String name, String lastName, String birthday, String mail) {
        this.id = id;
        this.name = name;
        this.lastName = lastName;
        this.birthday = birthday;
        this.mail = mail;
    }

    public String getId() {
        return id;
    }
}
